function wireUi(lang) {
  document.querySelector("[data-toggle-lang]")?.addEventListener("click", () => {
    const nextLang = lang === "ar" ? "en" : "ar";
    localStorage.setItem("lead-mines-lang", nextLang);
    render(nextLang);
  });

  document.querySelector("[data-toggle-theme]")?.addEventListener("click", () => {
    const nextTheme = root.dataset.theme === "dark" ? "light" : "dark";
    localStorage.setItem("lead-mines-theme", nextTheme);
    root.dataset.theme = nextTheme;
    render(lang);
  });

  const mobileMenu = document.querySelector("[data-mobile-menu]");
  document.querySelector("[data-mobile-menu-toggle]")?.addEventListener("click", () => {
    mobileMenu?.classList.add("is-open");
    document.body.classList.add("menu-open");
  });
  document.querySelector("[data-mobile-menu-close]")?.addEventListener("click", () => {
    mobileMenu?.classList.remove("is-open");
    document.body.classList.remove("menu-open");
  });

  const chatbotPanel = document.querySelector("[data-chatbot-panel]");
  document.querySelector("[data-chatbot-toggle]")?.addEventListener("click", () => chatbotPanel?.toggleAttribute("hidden"));
  document.querySelector("[data-chatbot-close]")?.addEventListener("click", () => chatbotPanel?.setAttribute("hidden", ""));
  document.querySelectorAll("[data-faq-answer]").forEach((button) => {
    button.addEventListener("click", () => {
      const response = document.querySelector("[data-chatbot-response]");
      if (response) response.textContent = button.getAttribute("data-faq-answer") || "";
    });
  });

  document.querySelectorAll("[data-demo-form]").forEach((form) => {
    form.addEventListener("submit", (event) => {
      event.preventDefault();
      alert(lang === "ar" ? "تم استلام البيانات محليًا. يمكن ربط النموذج لاحقًا بواتساب أو خدمة نماذج خارجية." : "The form was handled locally. You can later connect it to WhatsApp or an external form service.");
      form.reset();
    });
  });
}

function render(lang) {
  root.dataset.theme = localStorage.getItem("lead-mines-theme") || storedTheme;
  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  renderHeader(lang);
  renderFloating(lang);
  renderFooter(lang);
  if (page === "home") renderHome(lang);
  if (page === "blog") renderBlog(lang);
  if (page === "courses") renderCourses(lang);
  if (page === "giveaway") renderGiveaway(lang);
  if (page === "contact") renderContact(lang);
  wireUi(lang);
}

